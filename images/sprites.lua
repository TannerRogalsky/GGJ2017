function TextureAtlas:getDimensions(quadName)
	local quad = self.quads[quadName]
	if not quad then
		return nil
	end
	local x, y, w, h = quad:getViewport()
  return w, h
end

function TextureAtlas:getUVs(quadName)
  local quad = self.quads[quadName]
  local texture = self.texture
  local x, y, w, h = quad:getViewport()
  local iw, ih = texture:getDimensions()
  local ua, va = x / iw, y / ih
  local ub, vb = (x + w) / iw, (y + h) / ih
  return ua, va, ub, vb
end

